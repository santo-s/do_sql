CREATE USER DEMO
  IDENTIFIED BY DEMO
  -- 2 Roles for DEMO 
  GRANT CONNECT TO DEMO;
  GRANT RESOURCE TO DEMO;
  ALTER USER DEMO DEFAULT ROLE ALL;
  -- 1 System Privilege for DEMO 
  GRANT UNLIMITED TABLESPACE TO DEMO;

CREATE TABLE DEMO.BONUS
(
  ENAME  VARCHAR2(10 BYTE),
  JOB    VARCHAR2(9 BYTE),
  SAL    NUMBER,
  COMM   NUMBER
);

CREATE TABLE DEMO.DEPT
(
  DEPTNO  NUMBER(2),
  DNAME   VARCHAR2(14 BYTE),
  LOC     VARCHAR2(13 BYTE)
);


CREATE UNIQUE INDEX DEMO.PK_DEPT ON DEMO.DEPT
(DEPTNO);


ALTER TABLE DEMO.DEPT ADD (
  CONSTRAINT PK_DEPT
  PRIMARY KEY
  (DEPTNO)
  USING INDEX DEMO.PK_DEPT
  ENABLE VALIDATE);

CREATE TABLE DEMO.EMP
(
  EMPNO     NUMBER(4),
  ENAME     VARCHAR2(10 BYTE),
  JOB       VARCHAR2(9 BYTE),
  MGR       NUMBER(4),
  HIREDATE  DATE,
  SAL       NUMBER(7,2),
  COMM      NUMBER(7,2),
  DEPTNO    NUMBER(2)
);


CREATE UNIQUE INDEX DEMO.PK_EMP ON DEMO.EMP
(EMPNO);


ALTER TABLE DEMO.EMP ADD (
  CONSTRAINT PK_EMP
  PRIMARY KEY
  (EMPNO)
  USING INDEX DEMO.PK_EMP
  ENABLE VALIDATE);

CREATE TABLE DEMO.SALGRADE
(
  GRADE  NUMBER,
  LOSAL  NUMBER,
  HISAL  NUMBER
);


